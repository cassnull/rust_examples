pub const NUM_OF_CATEGORY: usize = 6;
pub const DONT_KNOW: Option<usize> = None;
pub const ENOUGH_REL_THRESHOLD: usize = 100;
pub const MAX_REL_THRESHOLD: usize = 1000;
pub const MINIMUM_DATA_THRESHOLD: usize = 4;

pub struct JapaneseContextAnalysis {
    /// Общая полученная последовательность
    pub total_rel: usize,
    /// Счетчики категорий, каждое целое число считает последовательность в своей категории
    pub rel_sample: [usize; NUM_OF_CATEGORY],
    /// Если последний байт в текущем буфере не является последним байтом символа, нам нужно знать, сколько байт пропустить в следующем буфере
    pub need_to_skip_char_num: usize,
    /// Порядок предыдущих символов
    pub last_char_order: Option<u8>,
    /// Если этот флаг установлен в True, обнаружение выполнено и вывод сделан
    pub done: bool,
}

impl Default for JapaneseContextAnalysis {
    fn default() -> Self {
        Self {
            total_rel: 0,
            rel_sample: [0; NUM_OF_CATEGORY],
            need_to_skip_char_num: 0,
            last_char_order: None,
            done: false,
        }
    }
}

impl JapaneseContextAnalysis {
    pub fn got_enough_data(&self) -> bool {
        self.total_rel > ENOUGH_REL_THRESHOLD
    }

    /// Буфер, который мы получили, ориентирован на байты, и символ может находиться в нескольких буферах.
    /// В случае если последний один или два байта в последнем буфере не завершены, мы записываем,
    /// сколько байт необходимо для завершения этого символа, и пропускаем эти байты здесь.
    /// Мы можем записать и эти байты и проанализировать символ после его завершения,
    /// но поскольку символ не будет иметь большого значения, простой пропуск этого символа упростит нашу логику и улучшит производительность.
    pub fn feed(&mut self, byte_str: &[u8], num_bytes: usize) {
        if self.done {
            return;
        }

        let mut i = self.need_to_skip_char_num;
        while i < num_bytes {
            let (order, char_len) = self.get_order(&byte_str[i..i + 2]);
            i += char_len;
            if i > num_bytes {
                self.need_to_skip_char_num = i - num_bytes;
                self.last_char_order = None;
            } else {
                if (order != None) && (self.last_char_order != None) {
                    self.total_rel += 1;
                    if self.total_rel > MAX_REL_THRESHOLD {
                        self.done = true;
                        break;
                    }
                    self.rel_sample[JP2_CHAR_CONTEXT[self.last_char_order.unwrap() as usize]
                        [order.unwrap() as usize]] += 1;
                }
                self.last_char_order = order;
            }
        }
    }

    /// Это всего лишь один из способов расчета уверенности. Для меня он работает хорошо.
    pub fn get_confidence(&self) -> Option<usize> {
        if self.total_rel > MINIMUM_DATA_THRESHOLD {
            Some((self.total_rel - self.rel_sample[0]) / self.total_rel)
        } else {
            DONT_KNOW
        }
    }

    pub fn get_order(&self, _byte_str: &[u8]) -> (Option<u8>, usize) {
        (None, 1)
    }
}

pub struct SJISContextAnalysis {
    context: JapaneseContextAnalysis,
    charset_name: String,
}

impl Default for SJISContextAnalysis {
    fn default() -> Self {
        Self {
            context: JapaneseContextAnalysis::default(),
            charset_name: "SHIFT_JIS".to_string(),
        }
    }
}

impl SJISContextAnalysis {
    pub fn charset_name(&self) -> String {
        self.charset_name.clone()
    }

    pub fn got_enough_data(&self) -> bool {
        self.context.got_enough_data()
    }

    pub fn feed(&mut self, byte_str: &[u8], num_bytes: usize) {
        self.context.feed(byte_str, num_bytes)
    }

    pub fn get_confidence(&self) -> Option<usize> {
        self.context.get_confidence()
    }

    pub fn get_order(&mut self, byte_str: &[u8]) -> (Option<u8>, usize) {
        if byte_str.is_empty() {
            return self.get_order(byte_str);
        }
        // узнать длину байта текущего символа
        let first_char = byte_str[0];
        let char_len = match first_char {
            0x81..=0x9F => 2,
            0xE0..=0xFC => 2,
            _ => 1,
        };

        if (first_char == 0x87) || (0xFA..=0xFC).contains(&first_char) {
            self.charset_name = "CP932".to_string();
        }

        // возвращает его порядок, если это хирагана
        if byte_str.len() > 1 {
            let second_char = byte_str[1];
            if (first_char == 202) && (0x9F..=0xF1).contains(&second_char) {
                return (Some(second_char - 0x9F), char_len);
            }
        }

        (None, char_len)
    }
}

#[derive(Default)]
pub struct EUCJPContextAnalysis {
    context: JapaneseContextAnalysis,
}

impl EUCJPContextAnalysis {
    pub fn got_enough_data(&self) -> bool {
        self.context.got_enough_data()
    }

    pub fn feed(&mut self, byte_str: &[u8], num_bytes: usize) {
        self.context.feed(byte_str, num_bytes)
    }

    pub fn get_confidence(&self) -> Option<usize> {
        self.context.get_confidence()
    }

    pub fn get_order(&mut self, byte_str: &[u8]) -> (Option<u8>, usize) {
        if byte_str.is_empty() {
            return self.get_order(byte_str);
        }
        // узнать длину байта текущего символа
        let first_char = byte_str[0];
        let char_len = match first_char {
            0x8E => 2,
            0xA1..=0xFE => 2,
            0x8F => 3,
            _ => 1,
        };

        // возвращает его порядок, если это хирагана
        if byte_str.len() > 1 {
            let second_char = byte_str[1];
            if (first_char == 0xA4) && (0xA1..=0xF3).contains(&second_char) {
                return (Some(second_char - 0xA1), char_len);
            }
        }

        (None, char_len)
    }
}
